public class Questoes2 {

    /**
     * @param args
     * 
     *             questão: Crie um programa que declare duas variáveis inteiras com
     *             os valores 15 e 25, calcule a soma e exiba o resultado.
     *             Objetivos**:
     *             - Declarar variáveis inteiras
     *             - Realizar operação de soma
     *             - Exibir resultado
     * 
     * @return "A soma de valor 1 e valor 2 é soma"
     */
    public static void main(String[] args) {
        int valor1 = 15;
        int valor2 = 25;
        int soma = valor1 + valor2;
        System.out.println("a soma de " + valor1 + " e " + valor2 + " é: " + soma);
    }
}
